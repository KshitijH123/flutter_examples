enum Color { red, green, blue, yellow }

void main() {
  Color myColor = Color.blue;

  switch (myColor) {
    case Color.red:
      print('The color is Red');
      break;
    case Color.green:
      print('The color is Green');
      break;
    case Color.blue:
      print('The color is Blue');
      break;
    case Color.yellow:
      print('The color is Yellow');
      break;
    }
       
}
